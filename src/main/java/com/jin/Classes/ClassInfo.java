package com.jin.Classes;

//클래스 정보 
public class ClassInfo {

 // 닉네임 
 private String nickname;

 // 클래스명 
 private String classname;

 // 장르 
 private String cgenre;

 // 상세장르 
 private String cdetailgenre;

 // 지역 
 private String ccity;

 // 모임유형 
 private Integer conline;

 // 비용 
 private Integer price;

 // 클래스설명 
 private String contents;

 // 클래스준비물 
 private String cmaterials;

 public String getNickname() {
     return nickname;
 }

 public void setNickname(String nickname) {
     this.nickname = nickname;
 }

 public String getClassname() {
     return classname;
 }

 public void setClassname(String classname) {
     this.classname = classname;
 }

 public String getCgenre() {
     return cgenre;
 }

 public void setCgenre(String cgenre) {
     this.cgenre = cgenre;
 }

 public String getCdetailgenre() {
     return cdetailgenre;
 }

 public void setCdetailgenre(String cdetailgenre) {
     this.cdetailgenre = cdetailgenre;
 }

 public String getCcity() {
     return ccity;
 }

 public void setCcity(String ccity) {
     this.ccity = ccity;
 }

 public Integer getConline() {
     return conline;
 }

 public void setConline(Integer conline) {
     this.conline = conline;
 }

 public Integer getPrice() {
     return price;
 }

 public void setPrice(Integer price) {
     this.price = price;
 }

 public String getContents() {
     return contents;
 }

 public void setContents(String contents) {
     this.contents = contents;
 }

 public String getCmaterials() {
     return cmaterials;
 }

 public void setCmaterials(String cmaterials) {
     this.cmaterials = cmaterials;
 }


}
